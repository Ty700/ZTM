# 4 Pillars
    # Encapsulation
        # Ability to encapsulate the attrubutes tied to a specific obj
        # Ability to encapsulate different methods that are only able to run via an obj
        
    # Abstraction 
        # The ability to use objects that have methods tied to them
        # The method works via a "black box", all we know from the outside is that the method does a certain function
        # Essentially not having to worry about how things work, they just do

    # Inheritence
        # Allows new objs to take on the properties of other objects
    
    # Polymorphism 
        # "Many forms"
        # The act of sharing method names, but act different depending on which object calls them
        # Example is the attack method in 04_Inheritence.py